#!/usr/bin/env bun
/**
 * Build-time script that generates an embedded asset map from src/ui/dist.
 * Output: src/ui/embedded-assets.js with static `with { type: "file" }` imports
 * and a request-path-to-embedded-file map for compiled binary serving.
 */
import fs from "node:fs";
import path from "node:path";

const DIST_DIR = path.resolve(import.meta.dir, "../src/ui/dist");
const OUTPUT_FILE = path.resolve(import.meta.dir, "../src/ui/embedded-assets.js");

function walk(dir, base = "") {
  const entries = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    if (entry.isDirectory()) {
      entries.push(...walk(path.join(dir, entry.name), rel));
    } else {
      entries.push(rel);
    }
  }
  return entries;
}

function mimeType(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  const types = {
    ".html": "text/html",
    ".js": "application/javascript",
    ".css": "text/css",
    ".svg": "image/svg+xml",
    ".json": "application/json",
    ".map": "application/json",
    ".png": "image/png",
    ".jpg": "image/jpeg",
    ".ico": "image/x-icon",
    ".woff": "font/woff",
    ".woff2": "font/woff2",
    ".ttf": "font/ttf",
  };
  return types[ext] || "application/octet-stream";
}

if (!fs.existsSync(DIST_DIR)) {
  console.error(`dist directory not found: ${DIST_DIR}`);
  console.error("Run 'bun run ui:build' first.");
  process.exit(1);
}

const files = walk(DIST_DIR);

// Generate import lines and map entries
const importLines = [];
const mapEntries = [];

files.forEach((relPath, i) => {
  const varName = `_asset${i}`;
  const importPath = `./dist/${relPath}`;
  importLines.push(`import ${varName} from ${JSON.stringify(importPath)} with { type: "file" };`);

  // Request path: "/" for index.html, "/assets/..." for asset files, etc.
  let requestPath = `/${relPath}`;
  mapEntries.push(
    `  ${JSON.stringify(requestPath)}: { path: ${varName}, mime: ${JSON.stringify(mimeType(relPath))} }`
  );
});

const output = `// Auto-generated by scripts/generate-embedded-assets.js — do not edit
${importLines.join("\n")}

export const embeddedAssets = {
${mapEntries.join(",\n")}
};
`;

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`Generated ${OUTPUT_FILE} with ${files.length} assets`);
